#include <iostream>
using namespace std;

int main()
{
    int Q, D;
    cout << "Enter two integers: ";
    cin >> Q >> D;

    cout << "Sum = " << Q + D << endl;
    cout << "Difference = " << Q - D << endl;
    cout << "Product = " << Q * D << endl;
    cout << "Quotient = " << Q / D << endl;

    return 0;
}
